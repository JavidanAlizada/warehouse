
package anbar;

 
public class Diagram3Bean {

      
    private double sum;
    private String ID;

    public Diagram3Bean(double sum, String ID) {
        this.sum = sum;
        this.ID = ID;
    }

    public double getSum() {
        return sum;
    }

    public String getID() {
        return ID;
    }
    
    
    
}
